#!/usr/bin/env node

import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(SCRIPT_DIR, "../..");
const OUTPUT_ASSET_DIR = path.join(
  REPO_ROOT,
  "public",
  "assets",
  "clubs",
  "premier-league",
);
const OUTPUT_DATA_FILE = path.join(REPO_ROOT, "data", "premierLeagueClubs.ts");

const PREMIER_LEAGUE_CLUBS = [
  { name: "Arsenal", slug: "arsenal" },
  { name: "Aston Villa", slug: "aston-villa" },
  { name: "Bournemouth", slug: "bournemouth" },
  { name: "Brentford", slug: "brentford" },
  { name: "Brighton & Hove Albion", slug: "brighton-hove-albion" },
  { name: "Burnley", slug: "burnley" },
  { name: "Chelsea", slug: "chelsea" },
  { name: "Crystal Palace", slug: "crystal-palace" },
  { name: "Everton", slug: "everton" },
  { name: "Fulham", slug: "fulham" },
  { name: "Leeds United", slug: "leeds-united" },
  { name: "Liverpool", slug: "liverpool" },
  { name: "Manchester City", slug: "manchester-city" },
  { name: "Manchester United", slug: "manchester-united" },
  { name: "Newcastle United", slug: "newcastle-united" },
  { name: "Nottingham Forest", slug: "nottingham-forest" },
  { name: "Sunderland", slug: "sunderland" },
  { name: "Tottenham Hotspur", slug: "tottenham-hotspur" },
  { name: "West Ham United", slug: "west-ham-united" },
  { name: "Wolverhampton Wanderers", slug: "wolverhampton-wanderers" },
];

async function main() {
  await mkdir(OUTPUT_ASSET_DIR, { recursive: true });

  const badgeMap = await fetchPremierLeagueBadgeMap();
  const generatedClubs = [];

  for (const club of PREMIER_LEAGUE_CLUBS) {
    process.stdout.write(`Fetching badge for ${club.name}...\n`);

    const badgeUrl = badgeMap.get(normalizeClubName(club.name));

    if (!badgeUrl) {
      throw new Error(`No badge found for ${club.name}`);
    }

    const imageResponse = await fetch(badgeUrl, {
      headers: {
        Accept: "image/*",
        "User-Agent": "caddyshack-club-generator/1.0",
      },
    });

    if (!imageResponse.ok) {
      throw new Error(
        `Failed to download badge for ${club.name}: ${imageResponse.status} ${imageResponse.statusText}`,
      );
    }

    const fileExtension = getFileExtension(
      imageResponse.headers.get("content-type"),
      badgeUrl,
    );
    const outputFileName = `${club.slug}${fileExtension}`;
    const outputFilePath = path.join(OUTPUT_ASSET_DIR, outputFileName);
    const imageBuffer = Buffer.from(await imageResponse.arrayBuffer());

    await writeFile(outputFilePath, imageBuffer);

    generatedClubs.push({
      name: club.name,
      slug: club.slug,
      badgePath: `/assets/clubs/premier-league/${outputFileName}`,
      badgeSourceUrl: badgeUrl,
    });
  }

  await writeFile(OUTPUT_DATA_FILE, renderClubDataFile(generatedClubs));

  process.stdout.write(
    `Generated ${generatedClubs.length} club records and badge assets.\n`,
  );
}

async function fetchPremierLeagueBadgeMap() {
  const response = await fetch(
    "https://www.thesportsdb.com/api/v1/json/3/search_all_teams.php?l=English%20Premier%20League",
    {
      headers: {
        Accept: "application/json",
        "User-Agent": "caddyshack-club-generator/1.0",
      },
    },
  );

  if (!response.ok) {
    throw new Error(
      `Failed to fetch Premier League team list: ${response.status} ${response.statusText}`,
    );
  }

  const payload = await response.json();
  const teams = payload?.teams ?? [];
  const badgeMap = new Map();

  for (const team of teams) {
    if (!team?.strTeam || !team?.strBadge) {
      continue;
    }

    badgeMap.set(normalizeClubName(team.strTeam), team.strBadge);
  }

  return badgeMap;
}

function getFileExtension(contentType, sourceUrl) {
  if (contentType?.includes("image/png")) {
    return ".png";
  }

  if (contentType?.includes("image/svg+xml")) {
    return ".svg";
  }

  if (contentType?.includes("image/webp")) {
    return ".webp";
  }

  if (contentType?.includes("image/jpeg")) {
    return ".jpg";
  }

  const urlPath = new URL(sourceUrl).pathname;
  const existingExtension = path.extname(urlPath);

  return existingExtension || ".png";
}

function renderClubDataFile(clubs) {
  const records = clubs
    .map(
      (club) => `  {
    name: ${JSON.stringify(club.name)},
    slug: ${JSON.stringify(club.slug)},
    badgePath: ${JSON.stringify(club.badgePath)},
    badgeSourceUrl: ${JSON.stringify(club.badgeSourceUrl)},
  },`,
    )
    .join("\n");

  return `// Generated by scripts/premier-league/generate-club-data.mjs
// Do not edit manually unless you are intentionally overriding generated output.

export type PremierLeagueClubRecord = {
  name: string;
  slug: string;
  badgePath: string;
  badgeSourceUrl: string;
};

export const premierLeagueClubs: PremierLeagueClubRecord[] = [
${records}
];
`;
}

function normalizeClubName(name) {
  return name
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/football club|fc|afc/g, "")
    .replace(/wanderers/g, "")
    .replace(/hotspur/g, "")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
